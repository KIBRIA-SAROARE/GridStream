def cordic_kernel_optimized(x, y, iterations=16):
    """
    Cordic rotation using pre-computed ROM tables.
    """
    if x == 0 and y == 0:
        return 0.0, 0.0

    current_x = x
    current_y = y
    current_angle = 0.0
    
    # 1. Quadrant correction (Hardware: simple sign check)
    if current_x < 0:
        current_x = -x
        current_y = -y
        current_angle = math.pi

    # 2. Iteration Loop (Hardware: Unrolled or Pipeline)
    for i in range(iterations):
        x_new = current_x
        y_new = current_y
        
        # Check sign of Y to determine direction
        d = 1.0 if current_y < 0 else -1.0
        
        # Bit shift operation equivalent to 2**(-i)
        factor = pow(2.0, -i)
        
        current_x = x_new - (d * y_new * factor)
        current_y = y_new + (d * x_new * factor)
        current_angle = current_angle - (d * CORDIC_ANGLES[i])

    magnitude = current_x * K_GAIN
    return magnitude, current_angle

